//Faça um Programa que peça a temperatura em graus Farenheit, transforme e mostre a temperatura em graus Celsius
//C = (5 * (F-32) / 9).


var Farenheit = parseFloat(prompt("Digite a temperatura em graus Farenheit"))

Celsius = (5 * (Farenheit-32) / 9)

console.log(Celsius)
alert("A temperatura esta á: " + Celsius + " graus Celsius")

